"""
本脚本用于演示 Python 的环境模型（Environment Model）与词法作用域（Lexical Scoping）。
核心教学点：函数的变量查找链取决于它“在哪里被定义”（Parent 环境），而非它“在哪里被调用”（Call Stack）。
"""

def f(x):
    """
    外层函数，用于创建一个嵌套的闭包环境。
    
    Args:
        x: 任意传入的数值。在调用 f 时，该值将被绑定在 f 的局部帧（Local Frame）中。
        
    Returns:
        function: 返回内部定义的函数对象 g，该对象死死绑定了当前 f 的局部帧作为其 Parent。
    """
    def g(y):
        """
        内层嵌套函数（闭包）。
        其 Parent 指向其出生时的环境，即包含变量 x 的 f(x) 局部帧。
        """
        # 1. 在当前 g 的局部帧中定义局部变量 n。
        # 这是一个故意设计的干扰项，用以证明处于全局的 h 函数在调用时无法访问到该帧。
        n = 6
        
        # 2. 触发变量查找与函数调用：
        # - y: 直接在当前 g 的局部帧中找到。
        # - x: 当前帧没有，沿着 Parent 链条倒推一步，在 f 的局部帧中找到。
        # - h: 当前帧与 f 的帧都没有，继续向外倒推，在 Global 帧中找到。
        return h(x) + y
        
    return g
    
def h(m):
    """
    定义在全局环境中的函数。
    由于其在全局被定义，它的 Parent 永远指向 Global Frame。
    
    Args:
        m: 接收来自外部传入的数值。
    """
    # 触发变量查找：
    # - m: 直接在当前 h 的局部帧中找到。
    # - n: 当前 h 的局部帧没有。
    #      【关键：词法作用域】h 不会顺着调用栈去查找 g 的局部帧（无视 n=6），
    #      而是直接顺着它的 Parent 链回到 Global Frame，从而找到全局变量 n=10。
    return m + n
    
# ================= 全局执行流 =================

# 在 Global Frame 中定义全局变量 n，值为 10
n = 10

# 1. 调用 f(1)：
#    - 创建一个 f 的局部帧（x = 1）。
#    - 在该环境中孵化出 g 函数对象，g 的 Parent 指向该帧。
#    - 将 g 函数对象的引用赋值给全局变量 func1。
func1 = f(1)

# 2. 调用 func1(2) 即调用 g(2)：
#    - 创建 g 的局部帧（y = 2, n = 6），其 Parent 指向 f 的局部帧。
#    - 计算 h(x) 即 h(1)：
#        - 创建 h 的局部帧（m = 1），其 Parent 指向 Global Frame。
#        - h 内部求值：m(1) + 全局的n(10) = 11。
#    - g 内部最终求值：h(1)的结果(11) + y(2) = 13。
result = func1(2)

# 打印最终结果，验证结果为 13
print(result)